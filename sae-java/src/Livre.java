public class Livre {
    private String isbn;
    private String titre;
    private int nbPages;
    private String datePubli;
    private double prix;

    public String getIsbn() {
        return isbn;
    }

    public String getTitre() {
        return titre;
    }

    public int getNbPages() {
        return nbPages;
    }

    public String getDatePubli() {
        return datePubli;
    }

    public double getPrix() {
        return prix;
    }
}
